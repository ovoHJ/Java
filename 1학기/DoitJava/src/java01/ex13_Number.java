package java01;

public class ex13_Number {

	public static void main(String[] args) {
		
		int num1 = 10;
		float num2 = 2.0f;
		
		System.out.println(num1 + " + " + num2 + " = " + (int)(num1 + num2));
		System.out.println(num1 + " - " + num2 + " = " + (int)(num1 - num2));
		System.out.println(num1 + " ¡¿ " + num2 + " = " + (int)(num1 * num2));
		System.out.println(num1 + " ¡À " + num2 + " = " + (int)(num1 / num2));

	}

}
